    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String rollNo= jTextField1.getText();
     int physicsMarks= Integer.parseInt(jTextField2.getText());
     int mathMarks= Integer.parseInt(jTextField3.getText());
     int chemMarks= Integer.parseInt(jTextField4.getText());
     int electricalMarks= Integer.parseInt(jTextField5.getText());
     int bioMarks= Integer.parseInt(jTextField6.getText());
     
     int total=   physicsMarks +  mathMarks + chemMarks + electricalMarks + bioMarks;
     
     try{
         Connection con= ConnectionProviderCode.getcon();
            Statement st= con.createStatement();
            st.executeUpdate("update result set physics="+ physicsMarks +", maths="+ mathMarks +", chemistry="+ chemMarks +",electrical="+ electricalMarks +", biology="+ bioMarks +", total="+ total +"  where rollNo="+ rollNo +"");
            JOptionPane.showMessageDialog(null, "Added Sucessfully");
            setVisible(false);
            UpdateResult frame= new UpdateResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
     }
     catch(Exception e){
         JOptionPane.showMessageDialog(null,e.toString());
     }
    }//GEN-LAST:event_jButton2ActionPerformed

